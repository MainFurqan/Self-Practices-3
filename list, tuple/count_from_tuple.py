# Count how many students got the grade 'A'
class9_result = ('C', 'F', 'A', 'B', 'B', 'C', 'A', 'B', 'A', 'A')

# Use the count() method to count occurrences of 'A'
grade_a_count = class9_result.count('A')

# Display the result
print(f"Number of students who got grade 'A': {grade_a_count}")



# Store the above class9_result in list and sort in ascending order
class9_result_list = ['C', 'F', 'A', 'B', 'B', 'C', 'A', 'B', 'A', 'A']
class9_result_list.sort() 
print(class9_result_list) # Display the sorted list